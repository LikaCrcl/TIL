CREATE TABLE items(
	icode CHAR(4) NOT NULL,
	name VARCHAR2(20) NOT NULL,
	detail VARCHAR2(30) NULL,
	price NUMBER(6) NULL,
	CONSTRAINT items_PK PRIMARY KEY(icode)
);

CREATE TABLE sale(
	idx NUMBER(5) NOT NULL,
	icode CHAR(4) NOT NULL,
	quantity NUMBER(4) NOT NULL,
	sal_date DATE NOT NULL,
	CONSTRAINT sale_FK FOREIGN KEY(icode) REFERENCES ITEMS(icode)
);

CREATE SEQUENCE sale_seq;

