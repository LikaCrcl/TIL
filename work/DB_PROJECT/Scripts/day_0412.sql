-- 오라클은 커서 위치에 따라 실행하는 부분이 달라짐. 또 그 커서 위치에 있는 명령만을 수행함. (자바처럼 처음부터 끝까지 실행하지 않음)

-- 테이블 생성(CREATE)
CREATE TABLE TEST(
	INTDATA NUMBER(4),
	STRDATA VARCHAR2(100)
);

-- 테이블 삭제(DROP)
DROP TABLE TEST;

-- 테이블 수정(ALTER)
-- 컬럼 추가
ALTER TABLE TEST ADD(
	DOUBLEDATA NUMBER(6,2)
);

-- 컬럼 수정
ALTER TABLE TEST MODIFY(
	STRDATA VARCHAR2(300)
);

-- 이름 변경
ALTER TABLE TEST RENAME TO TEST2;
ALTER TABLE TEST2 RENAME TO TEST;

-- 컬럼 이름 변경
ALTER TABLE TEST RENAME COLUMN DOUBLEDATA TO DOBDATA;

-- 컬럼 삭제
ALTER TABLE TEST DROP COLUMN DOBDATA;

DROP TABLE TEST;
