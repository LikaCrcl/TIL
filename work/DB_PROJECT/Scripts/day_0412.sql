-- 꽃 테이블
-- 꽃 이름(PK)
-- 꽃 색깔
-- 꽃 가격
CREATE TABLE FLOWER(
	FLOWERNAME VARCHAR2(10),
	FLOWERCOLOR VARCHAR2(5),
	FLOWERPRICE NUMBER(5),
	CONSTRAINT FLOWER_PK PRIMARY KEY(FLOWERNAME)
);

-- 화분 테이블
-- 화분 코드
-- 화분 모양
-- 화분 크기
-- 심을 꽃(FK)
-- 화분 가격
CREATE TABLE FLOWERPOT(
	FLOWERPOTCODE VARCHAR2(10),
	FLOWERPOTSHAPE VARCHAR2(10),
	FLOWERPOTSIZE VARCHAR2(10),
	FLOWERNAME VARCHAR2(10),
	FLOWERPOTPRICE NUMBER(5),
	CONSTRAINT FLOWERPOT_FLOWER_FK FOREIGN KEY(FLOWERNAME) REFERENCES FLOWER(FLOWERNAME)
);

DROP TABLE FLOWER;
DROP TABLE FLOWERPOT;
-- 영화 테이블
CREATE TABLE MOVIE(
